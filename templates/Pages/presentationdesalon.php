                            jouer un rôle de plus en plus important. Elles constituent un enjeu majeur pour les
                            prochaines années. Ce segment d’activité, qui est à promouvoir, a connu ces dernières années
                            une réelle dynamique et un important développement dans le cadre de la politique de
                            développement adoptée par le Gouvernement, orientée vers la diversification de l’économie
                            nationale.</p>

                        <p>Avec ses 9,5 millions d’hectares en zones de pêche et d’aquaculture exploitées et
                            exploitables, et ses 1600 km de ligne de côte qui abritent plus de 14 millions d’habitants,
